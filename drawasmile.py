import turtle
screen = turtle.Screen()
screen.setup(500,500)
t = turtle.Turtle()
screen.bgcolor("teal")
# t.done()
# Face
t.goto(0,-200)
t.fillcolor("white")
t.begin_fill()
t.pendown()
t.circle(200)
t.penup()
t.end_fill()
# Eye 1
t.goto(-50,-30)
t.pendown()
t.fillcolor("yellow")
t.begin_fill()
t.circle(50)
t.end_fill()
t.penup()
# Eye 2
t.goto(50,-30)
t.pendown()
t.fillcolor("yellow")
t.begin_fill()
t.circle(50)
t.end_fill()
t.penup()
# Mouth
t.goto(-100, -100)
t.pendown()
t.right(45)
t.circle(150,90)
t.penup()
# Hat
t.goto(-200, 150)
t.right(45)
t.fillcolor("red")
t.begin_fill()
t.pendown()
t.forward(400)
t.left(120)
t.forward(400)
t.left(120)
t.forward(400)
t.end_fill()
t.penup()

turtle.done()